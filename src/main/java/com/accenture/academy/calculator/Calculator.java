package com.accenture.academy.calculator;

public class Calculator {

    public Integer add(int x, int y){
        return x + y;
    }
    public Integer substract(int x, int y){
        return x - y;
    }
    public Integer multiply(int x, int y){
        return x * y;
    }
    public Integer divide(int x, int y){
        return x / y;
    }
}
